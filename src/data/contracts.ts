export function getContract(id: string): Contract | undefined {
  return contracts.find((c) => c.id === id);
}

export function getContractsSorted(by: 'urgency' | 'risk' | 'value' = 'urgency'): Contract[] {
  return [...contracts].sort((a, b) => {
    if (by === 'urgency') return a.daysToNextCritical - b.daysToNextCritical;
    if (by === 'risk') return b.riskScore - a.riskScore;
    return b.annualValueSAR - a.annualValueSAR;
  });
}
